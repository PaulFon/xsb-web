<p class="rubric">84.
The Priest, with hands extended, says: </p>

<p class="calibre6">To you, therefore, most merciful Father, we
make humble prayer and petition through Jesus Christ, your Son, our Lord: </p>

<p class="rubric">He joins his hands and says </p>

<p class="calibre6">that you accept</p>

<p class="rubric">He makes the Sign of the Cross once
over the bread and chalice together, saying: </p>

<p class="calibre6">and bless <span class="red1">+</span> these
gifts, these offerings,
these holy and unblemished sacrifices, </p>

<p class="rubric">With hands extended, he
continues: </p>

<p class="calibre6">which we offer you firstly for your holy
catholic Church.
Be pleased to grant her peace, to guard, unite and govern her
throughout the whole world,
together with your servant <span class="red1">Francis</span> our Pope and <span class="red1">N.</span> our Bishop,<span class="red1">*</span>
and all those who, holding to the truth,
hand on the catholic and apostolic faith. </p>

<p class="rubric">85.
Commemoration of the Living. </p>

<p class="calibre6">Remember, Lord, your servants <span class="red1">N.</span> and <span class="red1">N.</span></p>

<p class="rubric">The Priest joins his hands and
prays briefly for those for whom he intends to pray. Then, with hands extended,
he continues: </p>

<p class="calibre6">and all gathered here, whose faith and
devotion are known to you. For them, we offer you this sacrifice of praise or
they offer it for themselves and all who are dear to them: for the redemption
of their souls, in hope of health and well-being, and paying their homage to
you, the eternal God, living and true. </p>

<p class="rubric">86.
Within the Action.</p>

<p class="calibre6">In communion with those whose memory we
venerate,
especially the glorious ever-Virgin Mary,
Mother of our God and Lord, Jesus Christ,
and blessed Joseph, her Spouse,
your blessed Apostles and Martyrs,
Peter and Paul, Andrew, </p>

<p class="calibre6">
<span class="red1">(</span>James, John, Thomas, James, Philip,
Bartholomew, Matthew, Simon and Jude;
Linus, Cletus, Clement, Sixtus, Cornelius, Cyprian, Lawrence, Chrysogonus,
John and Paul, Cosmas and Damian<span class="red1">)
</span></p>

<p class="calibre6">and all your Saints;
we ask that through their merits and prayers,
in all things we may be defended by your protecting help. </p>

<p class="quietly"><span class="red1">(</span>Through Christ our Lord. Amen.<span class="red1">)</span> </p>

<p class="rubric">87.
With hands extended, the Priest continues: </p>

<p class="calibre6">Therefore, Lord, we pray:
graciously accept this oblation of our service,
that of your whole family;
order our days in your peace,
and command that we be delivered from eternal damnation
and counted among the flock of those you have chosen. </p>

<p class="rubric">He joins his hands. </p>

<p class="quietly"><span class="red1">(</span>Through Christ our Lord. Amen.<span class="red1">)</span> </p>

<p class="rubric">88.
Holding his hands extended over the offerings, he says: </p>

<p class="calibre6">Be pleased, O God, we pray,
to bless, acknowledge, and approve this offering in every respect;
make it spiritual and acceptable,
so that it may become for us
the Body and Blood of your most beloved Son,
our Lord Jesus Christ. </p>

<p class="rubric">He joins his hands. </p>

<p class="rubric">89. In the formulas that follow,
the words of the Lord should be pronounced clearly and distinctly, as the
nature of these words requires. </p>

<p class="calibre6">On the day before he was to suffer, </p>

<p class="rubric">He takes the bread and, holding
it slightly raised above the altar, continues: </p>

<p class="calibre6">he took bread in his holy and venerable
hands, </p>

<p class="rubric">He raises his eyes. </p>

<p class="calibre6">and with eyes raised to heaven to you,
O God, his almighty Father,
giving you thanks,
he said the blessing,
broke the bread and gave it to his disciples, saying: </p>

<p class="rubric">He bows slightly. </p>

<p class="calibre6">Take this, all of you, and eat of it, for this is my body, which will be given up for you. </p>

<p class="rubric">He shows the consecrated host to
the people, places it again on the paten, and genuflects in adoration. </p>

<p class="rubric">90. After this, the Priest
continues: </p>

<p class="calibre6">In a similar way, when supper was ended, </p>

<p class="rubric">He takes the chalice and,
holding it slightly raised above the altar, continues: </p>

<p class="calibre6">he took this precious chalice in his holy and
venerable hands, and once more giving you thanks, he said the blessing and gave the chalice to his disciples, saying: </p>

<p class="rubric">He bows slightly. </p>

<p class="calibre6"> Take this, all of you, and drink from it, for this is the chalice of my blood, the blood of the new and eternal covenant, which will be poured out for you and for many for the forgiveness of sins. </p>

<p class="calibre6">Do this in memory of me.</p>

<p class="rubric">He shows the chalice to the
people, places it on the corporal, and genuflects in adoration. </p>

<p class="rubric">91.
Then he says: </p>

<p class="calibre6">The mystery of faith. </p>

<p class="rubric">And the people continue,
acclaiming: </p>

<p class="response">We proclaim your Death, O
Lord,
and profess your Resurrection until you come again. </p>

<p class="rubric">or: </p>

<p class="response">When we eat this Bread and
drink this Cup,
we proclaim your Death, O Lord, until you come again. </p>

<p class="rubric">or: </p>

<p class="response">Save us, Savior of the world,

for by your Cross and Resurrection you have set us free. </p>

<p class="rubric">92.
Then the Priest, with hands extended, says: </p>

<p class="calibre6">Therefore, O Lord, as we celebrate the
memorial of the blessed Passion, the Resurrection from the dead,
and the glorious Ascension into heaven of Christ,
your Son, our Lord,
we, your servants and your holy people,
offer to your glorious majesty from the gifts that you have given us, this pure
victim, this holy victim,
this spotless victim,
the holy Bread of eternal life
and the Chalice of everlasting salvation. </p>

<p class="rubric">93. </p>

<p class="calibre6">Be pleased to look upon these offerings
with a serene and kindly countenance,
and to accept them,
as once you were pleased to accept
the gifts of your servant Abel the just,
the sacrifice of Abraham, our father in faith,
and the offering of your high priest Melchizedek,
a holy sacrifice, a spotless victim. </p>

<p class="rubric">94. Bowing, with hands joined,
he continues: </p>

<p class="calibre6">In humble prayer we ask you, almighty God:
command that these gifts be borne
by the hands of your holy Angel
to your altar on high in the sight of your divine majesty,
so that all of us,
who through this participation at the altar
receive the most holy Body and Blood of your Son, </p>

<p class="rubric">He stands upright again and
signs himself with the Sign of the Cross, saying: </p>

<p class="calibre6">may be filled with every grace and heavenly
blessing. </p>

<p class="rubric">He joins his hands. </p>

<p class="quietly"><span class="red1">(</span>Through Christ our Lord. Amen.<span class="red1">)</span> </p>

<p class="rubric">95.
Commemoration of the Dead </p>

<p class="rubric">With hands extended, the Priest
says: </p>

<p class="calibre6">Remember also, Lord, your servants <span class="red1">N.</span> and <span class="red1">N.</span>,
who have gone before us with the sign of faith
and rest in the sleep of peace. </p>

<p class="rubric">He joins his hands and prays
briefly for those who have died and for whom he intends to pray. </p>

<p class="rubric">Then, with hands extended, he
continues: </p>

<p class="calibre6">Grant them, O Lord, we pray,
and all who sleep in Christ,
a place of refreshment, light and peace. </p>

<p class="rubric">He joins his hands. </p>

<p class="quietly"><span class="red1">(</span>Through Christ our Lord. Amen.<span class="red1">)</span> </p>

<p class="rubric">96.
He strikes his breast with his right hand, saying: </p>

<p class="calibre6">To us, also, your servants, who, though
sinners, </p>

<p class="rubric">And, with hands extended, he
continues: </p>

<p class="calibre6">hope in your abundant mercies,
graciously grant some share and fellowship
with your holy Apostles and Martyrs:
with John the Baptist, Stephen, Matthias, Barnabas, </p>

<p class="calibre6"><span class="red1">(</span>Ignatius,
Alexander, Marcellinus, Peter,
Felicity, Perpetua, Agatha,
Lucy, Agnes, Cecilia, Anastasia<span class="red1">)</span> </p>

<p class="calibre6">and all your Saints; admit us,
we beseech you, into their company,
not weighing our merits, but granting us your pardon, </p>

<p class="rubric">He joins his hands. </p>

<p class="calibre6">through Christ our Lord. </p>

<p class="rubric">97.
And he continues: </p>

<p class="calibre6">Through whom you continue to make
all these good things, O Lord;
you sanctify them, fill them with life,
bless them, and bestow them upon us. </p>

<p class="rubric">98.
He takes the chalice and the paten with the host and, raising both, he says: </p>

<p class="calibre6">Through him, and with him, and in him,
O God, almighty Father,
in the unity of the Holy Spirit,
all glory and honor is yours,
for ever and ever.</p>

<p class="rubric">The people acclaim: </p>

<p class="response">Amen. </p>
